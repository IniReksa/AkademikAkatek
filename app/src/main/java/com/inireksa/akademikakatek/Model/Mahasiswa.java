public class Mahasiswa {

    public String nama;
    public String kelas;
    public int nohp;
    public int npm;
    public int img;

    public Mahasiswa(String nama, String kelas, int nohp, int npm, int img) {
        this.nama = nama;
        this.kelas = kelas;
        this.nohp = nohp;
        this.npm = npm;
        this.img = img;
    }
}
